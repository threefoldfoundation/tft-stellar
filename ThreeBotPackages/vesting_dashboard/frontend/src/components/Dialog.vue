<template>
  <v-dialog v-model="dialog" width="700">
    <v-card :loading="loading" :disabled="loading">
      <v-card-title class="headline">{{ data.name }}</v-card-title>
      <v-card-text class="pa-5">
        <v-alert v-if="error" dense outlined type="error" class="mb-5">
          {{ error }}
        </v-alert>

        <slot name="default"></slot>
      </v-card-text>

      <v-card-actions>
        <v-spacer></v-spacer>
        <slot name="actions"></slot>
      </v-card-actions>
    </v-card>
  </v-dialog>
</template>

<script>
export default {
  name: "Dialog",
  props: {
    value: Boolean,
    title: String,
    error: String,
    loading: Boolean,
    data: Object,
  },
  computed: {
    dialog: {
      get() {
        return this.value;
      },
      set(value) {
        this.$emit("input", value);
      },
    },
  },
};
</script>